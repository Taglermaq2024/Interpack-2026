               <p>ULMA Packaging  una de las empresas de envasado más grandes del mundo, desde ya hace un tiempo, se ha propuesto desarrollar tecnologías dirigidas a reducir el uso de plástico, conscientes del crecimiento de su uso y del innegable impacto que generan los residuos en el medioambiente.</p>

               <p>La sociedad en general se ha acostumbrado a un modelo de producción y consumo lineal, generador de residuos. Este es un modelo abocado a desaparecer y a ser sustituido por un modelo de Economía Circular, en el que los residuos sean tratados como recursos a valorar y monetizar.</p>

